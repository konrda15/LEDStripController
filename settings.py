class ChSettings:
	def __init__(self, variation, speed, color1, color2, color3):
		self.variation = variation
		self.speed = speed
		self.color1 = color1
		self.color2 = color2
		self.color3 = color3
	
class ModeSettings:
	def __init__(self, mode, brightness):
		self.mode = mode
		self.brightness = brightness
